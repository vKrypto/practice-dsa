from typing import Optional, List



class TreeNode:  # pragma: no cover
    def __init__(self, val=0, left=None, right=None) -> Optional(List):
        self.val = val
        self.left = left
        self.right = right



class ListNode:  # pragma: no cover
    def __init__(self, val=0, next=None):
        self.val = val
        self.next = next
